$('.rcs-highlights').click(function() {
    var $this = $(this);
    $this.toggleClass('rcs-highlights');
    if ($this.hasClass('rcs-highlights')) {
        $this.text('Project highlights');
    } else {
        $this.text('Collapse');
    }
});

